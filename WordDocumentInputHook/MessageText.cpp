std::string GetMessageName(UINT msg)
{
	for (int i = 0; i < sizeof(g_msgMap) / sizeof(g_msgMap[0]); ++i) {
		if (g_msgMap[i].msg == msg)
			return g_msgMap[i].name;
	}
	return std::to_string(msg);
}